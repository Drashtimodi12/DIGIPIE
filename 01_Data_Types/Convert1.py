# Convert Datatypes
# Convert:
# integer 5 into string
a = 5
b = str(a)
print(b, type(b))           # 5 <class 'str'>

print("------------------------")

# string "25" into integer
a = '25'
b = int(a)
print(b, type(b))       # 25 <class 'int'>

print("------------------------")

# float 12.67 into integer
a = 12.67
b = int(a)
print(b, type(b))           # 12 <class 'int'>

print("------------------------")

# list [1,2,3] into tuple
a = [1, 2, 3]
b = tuple(a)
print(b, type(b))           # (1, 2, 3) <class 'tuple'>

