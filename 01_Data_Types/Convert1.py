# Convert Datatypes
# Convert:
# integer 5 into string
a = 5
b = str(a)
print(b, type(b))

print("------------------------")

# string "25" into integer
a = '25'
b = int(a)
print(b, type(b))

print("------------------------")

# float 12.67 into integer
a = 12.67
b = int(a)
print(b, type(b))

print("------------------------")

# list [1,2,3] into tuple
a = [1, 2, 3]
b = tuple(a)
print(b, type(b))
